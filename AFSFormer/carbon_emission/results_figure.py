def gross_carbon_emissions(list1, list2, list3, list4):
    leng = len(list1)
    list_all = []
    for i in range(leng):
        print('number:', i, '   emissions:', list1[i]+list2[i]+list3[i]+list4[i])
        list_all.append(list1[i]+list2[i]+list3[i]+list4[i])

    return list_all


def average_carbon_emissions(list):
    leng =  len(list)
    list_average = []
    for i in range(leng):
        temp = list[i] // 4
        list_average.append(temp)

    return list_average

